    public static void setValoresTabla(String id, String mesDia, String hora, String clasificacion, String prioridad, String significado) {
        DefaultTableModel modelo = (DefaultTableModel) jTableAlertas.getModel();
        Object[] fila = new Object[6];
        fila[0] = id;
        fila[1] = mesDia;
        fila[2] = hora;
        fila[3] = clasificacion;
        fila[4] = prioridad;
        fila[5] = significado;

        modelo.addRow(fila);
        jTableAlertas.setModel(modelo);

    }

    public void limpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTableAlertas.getModel();
        modelo.setRowCount(0);
        jTableAlertas.setModel(modelo);
    }

