    private void jbtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBuscarActionPerformed

        limpiarTabla();
        alertas = new Snort(archivo);
        alertas.ordenarArchivo();
        
        String busquedaClasificacion = "[Classification: " + jtfAleClasif.getText().toString();
        String busquedaPrioridad = "[Priority: " + jcomAlePrioridad.getSelectedItem().toString();
        String busquedaId = "ID: " + jtfID.getText().toString();
        String busquedaMesDia = jtfMesDia.getText().toString();
        String busquedaHora = jtfHora.getText().toString();
        
        if (!(jtfID.getText().isEmpty())) {
            alertas.busqueda(busquedaId);
        }else if(jcomAlePrioridad.getSelectedIndex() != 0){
            alertas.busqueda(busquedaPrioridad);
        }else if(!(jtfAleClasif.getText().isEmpty())){
            alertas.busqueda(busquedaClasificacion);
        }else if (!(jtfMesDia.getText().isEmpty())) {
            alertas.busqueda(busquedaMesDia);
        }else if(!(jtfHora.getText().isEmpty())){
            alertas.busqueda(busquedaHora);
        }     
    }//GEN-LAST:event_jbtnBuscarActionPerformed

