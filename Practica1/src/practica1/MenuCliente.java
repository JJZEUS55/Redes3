    public static void setValoresTabla(Cliente cli) {
        DefaultTableModel modelo = (DefaultTableModel) jTableArchivos.getModel();
        Object[] fila = new Object[3];
        fila[0] = cli.getDireccionIP();
        fila[1] = cli.getNombreArchivo();
        fila[2] = cli.getTamArchivo();
        modelo.addRow(fila);
        jTableArchivos.setModel(modelo);
    }

